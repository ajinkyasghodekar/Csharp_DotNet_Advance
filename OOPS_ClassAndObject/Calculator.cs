public class Calculator
    {
        public void Add(int num1, int num2)
        {
            int result;
            Console.WriteLine("This is addition: ");
            result = num1 + num2;
            Console.WriteLine(result);
        }

        public void Sub(int num1, int num2)
        {
            int result;
            Console.WriteLine("This is substraction: ");
            result = num1 - num2;
            Console.WriteLine(result);
        }
    }